#include <stdio.h>
int main(){

    int a = 4;
    printf("The Value of 'a' post-increment:%d\n", a++);
    printf("After post-increment the current value of 'a' is :%d\n", a);
    printf("The Value of 'a' pre-increment:%d\n", ++a);
    printf("After pre-increment the value of 'a' now is :%d\n", a);

return 0;
}